ATTRIBUTES = dict(
        list(zip([
            'bold',
            'dark',
            '',
            'underline',
            'blink',
            '',
            'reverse',
            'concealed'
            ],
            list(range(1, 9))
            ))
        )
del ATTRIBUTES['']


HIGHLIGHTS = dict(
        list(zip([
            'on_grey',
            'on_red',
            'on_green',
            'on_yellow',
            'on_blue',
            'on_magenta',
            'on_cyan',
            'on_white'
            ],
            list(range(40, 48))
            ))
        )


COLORS = dict(
        list(zip([
            'grey',
            'red',
            'green',
            'yellow',
            'blue',
            'magenta',
            'cyan',
            'white',
            ],
            list(range(30, 38))
            ))
        )


RESET = '\033[0m'


def colored(text, color=None, on_color=None, attrs=None):
    ftext = ""
    fmt_str = '\033[%dm'
    if color is not None:
        ftext += fmt_str % COLORS[color]

    if on_color is not None:
        ftext += fmt_str % HIGHLIGHTS[on_color]

    if attrs is not None:
        for attr in attrs:
            ftext += fmt_str % ATTRIBUTES[attr]
    ftext += text + RESET
    return ftext
